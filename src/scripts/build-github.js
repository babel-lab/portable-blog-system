// Phase related-links-ga4-audit：對 relatedLinks / otherLinks 預處理。
//   - 對 Blogger cross-link 連結注入 UTM + 設 target=_blank + 合併 rel（per ga4-url-builder.applyCrossSiteUtm）
//   - 非 Blogger 連結維持原樣（item.target / item.rel 為 null；EJS template 走 fallback 既有邏輯）
//   - 非 object / 非 string url 之 item 維持原樣（EJS render-time filter 會 skip）
function deriveRenderedCrossLinks(rawLinks, settings, slot) {
  const arr = Array.isArray(rawLinks) ? rawLinks : [];
  return arr.map((item) => {
    if (!item || typeof item !== 'object' || Array.isArray(item)) return item;
    if (typeof item.url !== 'string' || item.url.trim() === '') return item;
    const xs = applyCrossSiteUtm({
      url: item.url,
      settings,
      slot,
      existingRel: typeof item.rel === 'string' ? item.rel : '',
    });
    if (xs.target === null) return item; // 非 Blogger cross-link：不動
    return { ...item, url: xs.url, target: xs.target, rel: xs.rel };
  });
}

function buildSeoNoindex({ pageType, settings }) {
  return {
    ...commonSeo({ pageType, settings, canonicalUrl: null }),
    robots: 'noindex, nofollow',
  };
}

async function main() {
  const mode = parseMode(process.argv.slice(2));
  const startedAt = new Date();
  const outputs = [];

  // Phase Admin-1-b：每次 run 都清除 .cache/pages/admin/ 殘留；dev-mode 之後會 re-create
  //   - 防止 prod build 因 stale .cache 而把 admin/index.html 帶進 dist/
  //   - 即使 vite emptyOutDir 清 dist，.cache 不被清，故需 build-github.js 主動處理
  const adminCacheDir = path.join(PAGES_DIR, 'admin');
  await fs.rm(adminCacheDir, { recursive: true, force: true }).catch(() => {});

  const settings = await loadSettings();
  // Phase 8-f-2-b：plumbing — settings 經 loadPosts 轉發至 processMarkdownEntry / normalizePostOutput
  // Phase 9-i-f-b：改用 loadGithubPosts() cross-source aggregator；mirror loadBloggerPosts 既有模式
  //   含 github 原生 source + content/blogger/posts 中 publishTargets.github.enabled === true 之 cross-source
  const githubPosts = await loadGithubPosts({ settings });
  const baseData = makeBaseData(settings);

  const { warnings } = validateContent({ posts: githubPosts.posts, settings });

  const postsOutput = {
    buildAt: startedAt.toISOString(),
    mode,
    site: 'github',
    siteName: settings.site.siteName,
    totalScanned: githubPosts.totalScanned,
    totalReady: githubPosts.totalReady,
    totalFiltered: githubPosts.totalFiltered,
    posts: githubPosts.posts,
    filteredOut: githubPosts.filteredOut,
  };
  await writeJson(path.join(DATA_DIR, 'posts.json'), postsOutput, outputs);
  await writeJson(path.join(DATA_DIR, 'site.json'), settings, outputs);

  await writeText(path.join(ASSETS_DIR, 'entry.js'), ENTRY_JS, outputs);

  const homeHtml = await renderPage({
    template: 'pages/home.ejs',
    data: baseData({
      posts: githubPosts.posts,
      title: settings.site.siteName,
      description: settings.site.description,
      seo: buildSeoForHome({ settings }),
    }),
  });
  await writeText(path.join(PAGES_DIR, 'index.html'), homeHtml, outputs);

  const listHtml = await renderPage({
    template: 'pages/post-list.ejs',
    data: baseData({
      posts: githubPosts.posts,
      title: `文章列表 | ${settings.site.siteName}`,
      description: `${settings.site.siteName} 文章列表`,
      seo: buildSeoForPostList({ settings }),
    }),
  });
  await writeText(path.join(PAGES_DIR, 'posts', 'index.html'), listHtml, outputs);

  for (const post of githubPosts.posts) {
    const bodyHtml = renderBody(post.body || '');
    // Phase related-links-ga4-audit：對每篇 post 預處理 relatedLinks / otherLinks
    //   - 對 Blogger cross-link 注入 GA4 UTM + 強制 target=_blank + 合併 rel
    //   - 非 Blogger 連結 / 同站連結維持 EJS 預設邏輯（item.target / item.rel 為 null）
    //   - 既有 utm_* 套用策略 A：跳過 UTM 注入但仍套 target/rel
    const relatedLinksRendered = deriveRenderedCrossLinks(post.relatedLinks, settings, 'related_links');
    const otherLinksRendered = deriveRenderedCrossLinks(post.otherLinks, settings, 'other_links');
    const detailHtml = await renderPage({
      template: 'pages/post-detail.ejs',
      data: baseData({
        post,
        // Phase 8-d-3a：additive alias for EJS ergonomics；指向 8-d-2 掛載之 post.normalized。
        //   - 不重新呼叫 normalizePostOutput；不啟用 deriveGithubUrl；不預測 Blogger URL
        //   - EJS template 本批不改讀 normalized；屬 8-d-3b 之後範圍
        //   - 既有 EJS 仍讀 post.X；本欄位 additive，不影響輸出
        normalized: post.normalized,
        bodyHtml,
        title: `${post.title} | ${settings.site.siteName}`,
        description: post.description || settings.site.description,
        seo: buildSeoForPostDetail({ settings, post }),
        relatedLinksRendered,
        otherLinksRendered,
      }),
    });
    await writeText(path.join(PAGES_DIR, 'posts', post.slug, 'index.html'), detailHtml, outputs);
  }

  const findCategory = (ref) =>
    settings.categories.find((c) => c.id === ref) ||
    settings.categories.find((c) => c.slug === ref) ||
    null;
  const findTag = (ref) =>
    settings.tags.find((t) => t.id === ref) ||
    settings.tags.find((t) => t.slug === ref) ||
    null;

  const categoryMap = new Map();
  for (const post of githubPosts.posts) {
    if (!post.category) continue;
    const cat = findCategory(post.category);
    if (!cat) continue;
    if (!categoryMap.has(cat.slug)) categoryMap.set(cat.slug, { category: cat, posts: [] });
    categoryMap.get(cat.slug).posts.push(post);
  }
  for (const [slug, group] of categoryMap) {
    const html = await renderPage({
      template: 'pages/category.ejs',
      data: baseData({
        category: group.category,
        posts: group.posts,
        title: `分類：${group.category.name} | ${settings.site.siteName}`,
        description: `${settings.site.siteName} 分類「${group.category.name}」文章列表`,
        seo: buildSeoForCategory({ settings, group }),
      }),
    });
    await writeText(path.join(PAGES_DIR, 'categories', slug, 'index.html'), html, outputs);
  }

  const categoryEntries = [];
  for (const cat of settings.categories) {
    const group = categoryMap.get(cat.slug);
    if (group && group.posts.length > 0) {
      categoryEntries.push({ slug: cat.slug, name: cat.name, count: group.posts.length });
    }
  }
  const categoryListHtml = await renderPage({
    template: 'pages/category-list.ejs',
    data: baseData({
      entries: categoryEntries,
      title: `分類索引 | ${settings.site.siteName}`,
      description: `${settings.site.siteName} 分類索引`,
      seo: buildSeoForCategoryList({ settings }),
    }),
  });
  await writeText(path.join(PAGES_DIR, 'categories', 'index.html'), categoryListHtml, outputs);

  const tagMap = new Map();
  for (const post of githubPosts.posts) {
    for (const ref of post.tags || []) {
      const t = findTag(ref);
      if (!t) continue;
      if (!tagMap.has(t.slug)) tagMap.set(t.slug, { tag: t, posts: [] });
      tagMap.get(t.slug).posts.push(post);
    }
  }
  for (const [slug, group] of tagMap) {
    const html = await renderPage({
      template: 'pages/tag.ejs',
      data: baseData({
        tag: group.tag,
        posts: group.posts,
        title: `標籤：${group.tag.name} | ${settings.site.siteName}`,
        description: `${settings.site.siteName} 標籤「${group.tag.name}」文章列表`,
        seo: buildSeoForTag({ settings, group }),
      }),
    });
    await writeText(path.join(PAGES_DIR, 'tags', slug, 'index.html'), html, outputs);
  }

  const tagEntries = [];
  for (const tag of settings.tags) {
    const group = tagMap.get(tag.slug);
    if (group && group.posts.length > 0) {
      tagEntries.push({ slug: tag.slug, name: tag.name, count: group.posts.length });
    }
  }
  const tagListHtml = await renderPage({
    template: 'pages/tag-list.ejs',
    data: baseData({
      entries: tagEntries,
      title: `標籤索引 | ${settings.site.siteName}`,
      description: `${settings.site.siteName} 標籤索引`,
      seo: buildSeoForTagList({ settings }),
    }),
  });
  await writeText(path.join(PAGES_DIR, 'tags', 'index.html'), tagListHtml, outputs);

  const notFoundHtml = await renderPage({
    template: 'pages/404.ejs',
    data: baseData({
      title: `找不到頁面 | ${settings.site.siteName}`,
      description: '找不到頁面',
      seo: buildSeoNoindex({ pageType: '404', settings }),
    }),
  });
  await writeText(path.join(PAGES_DIR, '404.html'), notFoundHtml, outputs);

  const designSubpages = [
    { slug: 'colors', name: 'Colors', code: 'G-02' },
    { slug: 'spacing', name: 'Spacing', code: 'G-03' },
    { slug: 'typography', name: 'Typography', code: 'G-04' },
    { slug: 'buttons', name: 'Buttons', code: 'G-05' },
    { slug: 'cards', name: 'Cards', code: 'G-06' },
    { slug: 'article-components', name: 'Article Components', code: 'G-07' },
  ];

  const designSystemHtml = await renderPage({
    template: 'design-system/index.ejs',
    data: baseData({
      title: `Design System | ${settings.site.siteName}`,
      description: `${settings.site.siteName} Design System`,
      subpages: designSubpages,
      seo: buildSeoNoindex({ pageType: 'design-system', settings }),
    }),
  });
  await writeText(path.join(PAGES_DIR, 'design-system', 'index.html'), designSystemHtml, outputs);

  for (const sub of designSubpages) {
    const html = await renderPage({
      template: `design-system/${sub.slug}.ejs`,
      data: baseData({
        title: `${sub.code} ${sub.name} | Design System | ${settings.site.siteName}`,
        description: `${settings.site.siteName} Design System - ${sub.name}`,
        seo: buildSeoNoindex({ pageType: 'design-system', settings }),
      }),
    });
    await writeText(path.join(PAGES_DIR, 'design-system', sub.slug, 'index.html'), html, outputs);
  }

  // Phase Admin-1-b：dev-mode-only read-only Admin page
  //   - 僅 mode === 'dev' 時 render；prod build 不產出（per admin-local-boundary §3 + admin-1-readonly-preflight Plan B）
  //   - 直接 ejs.renderFile 不 wrap base.ejs（admin 為 standalone HTML；無 nav / GA4 / Ads）
  //   - 寫入 .cache/pages/admin/index.html → vite dev serve 於 /admin/
  //   - 不進 sitemap（build-sitemap.js 不掃 admin）；不進 deploy（cp dist 時無此目錄；prod build 已早 rm）
  if (mode === 'dev') {
    const adminData = await loadAdminPosts({ settings });
    const adminHtml = await ejs.renderFile(
      path.join(VIEWS_DIR, 'admin', 'index.ejs'),
      { posts: adminData.posts, builtAt: startedAt.toISOString() },
      { async: true },
    );
    await writeText(path.join(PAGES_DIR, 'admin', 'index.html'), adminHtml, outputs);
    console.log(`[build-github] admin (dev-mode) rendered: ${adminData.posts.length} posts`);
  }

  const manifest = {
    buildAt: startedAt.toISOString(),
    mode,
    site: 'github',
    totals: {
      scanned: githubPosts.totalScanned,
      ready: githubPosts.totalReady,
      filtered: githubPosts.totalFiltered,
      categoryPages: categoryMap.size,
      tagPages: tagMap.size,
    },
    validate: { warnings: warnings.length },
    outputs: [...outputs, rel(path.join(DATA_DIR, 'build-manifest.json'))],
  };
  await writeJson(path.join(DATA_DIR, 'build-manifest.json'), manifest);

  const elapsed = Date.now() - startedAt.getTime();
  console.log(`[build-github] mode=${mode}`);
  console.log(`[build-github] scanned ${postsOutput.totalScanned}, ready ${postsOutput.totalReady}, filtered ${postsOutput.totalFiltered}`);
  if (postsOutput.filteredOut.length > 0) {
    for (const f of postsOutput.filteredOut) {
      console.log(`[build-github]   filtered: ${f.sourcePath} (${f.reason})`);
    }
  }
  printWarnings(warnings);
  for (const o of manifest.outputs) {
    console.log(`[build-github] wrote ${o}`);
  }
  console.log(`[build-github] done in ${elapsed}ms`);
}

main().catch((err) => {
  console.error('[build-github] failed:', err);
  process.exit(1);
});
