function parseMode(argv) {
  const flag = argv.find((a) => a.startsWith('--mode='));
  if (!flag) return 'dev';
  return flag.split('=')[1] || 'dev';
}

const rel = (p) => path.relative(PROJECT_ROOT, p).split(path.sep).join('/');

async function writeJson(file, data, outputs) {
  await fs.mkdir(path.dirname(file), { recursive: true });
  await fs.writeFile(file, JSON.stringify(data, null, 2), 'utf-8');
  if (outputs) outputs.push(rel(file));
}

async function writeText(file, content, outputs) {
  await fs.mkdir(path.dirname(file), { recursive: true });
  await fs.writeFile(file, content, 'utf-8');
  if (outputs) outputs.push(rel(file));
}

// Phase 5-b：渲染 SEO partials 後 post-process 注入 base.ejs 產出的 HTML 之 head 內。
// 採用 post-process 而非修改 base.ejs，避開未授權檔案修改。
// Phase 5-d：擴充加入 tracking/ga4 partial，邏輯一致。
const HEAD_PARTIALS = [
  { dir: 'seo', name: 'meta-tags' },
  { dir: 'seo', name: 'open-graph' },
  { dir: 'seo', name: 'canonical' },
  { dir: 'seo', name: 'json-ld' },
  { dir: 'tracking', name: 'ga4' },
  { dir: 'ads', name: 'adsense-head' },
];

async function renderHeadPartials(data) {
  const parts = [];
  for (const { dir, name } of HEAD_PARTIALS) {
    const html = await ejs.renderFile(
      path.join(VIEWS_DIR, dir, `${name}.ejs`),
      data,
      { async: true },
    );
    const trimmed = html.trim();
    if (trimmed) parts.push(trimmed);
  }
  return parts.join('\n');
}

async function renderPage({ template, data }) {
  const inner = await ejs.renderFile(path.join(VIEWS_DIR, template), data, { async: true });
  const wrapped = await ejs.renderFile(
    path.join(VIEWS_DIR, 'layout', 'base.ejs'),
    { ...data, body: inner },
    { async: true },
  );
  const headPartials = await renderHeadPartials(data);
  if (!headPartials) return wrapped;
  return wrapped.replace('</head>', `    ${headPartials.replace(/\n/g, '\n    ')}\n  </head>`);
}

function makeBaseData(settings) {
  const basePath = siteBasePath(settings);
  return (extra) => ({
    site: settings.site,
    navigation: settings.navigation,
    ga4: settings.ga4,
    ads: settings.ads,
    basePath,
    ...extra,
  });
}

// ---- 5-b SEO helpers --------------------------------------------------------

function siteBaseUrl(settings) {
  return (settings.site?.githubSiteUrl || '').replace(/\/+$/, '');
}

// Phase 10-e-fix-a：derive in-site base path from githubSiteUrl for project-site deploy.
//   user site (e.g. https://babel-lab.github.io)         → ''
//   project site (e.g. https://babel-lab.github.io/blog)  → '/blog'
//   consumed by EJS as <%= basePath %>{root-relative href} for internal navigation
function siteBasePath(settings) {
  const url = settings.site?.githubSiteUrl || '';
  if (!url) return '';
  try {
    return new URL(url).pathname.replace(/\/+$/, '');
  } catch (e) {
    return '';
  }
}

function ogLocaleFromLanguage(language) {
  if (language === 'zh-Hant') return 'zh_TW';
  if (language === 'zh-Hans') return 'zh_CN';
  if (language === 'en' || language === 'en-US') return 'en_US';
  return language || null;
}

function absolutizeUrl(url, base) {
  if (!url) return null;
  if (/^https?:\/\//.test(url)) return url;
  if (!base) return null;
  const cleanPath = url.replace(/^\/+/, '');
  return `${base}/${cleanPath}`;
}

function findCategoryName(settings, value) {
  if (!value) return null;
  const list = settings.categories || [];
  const cat = list.find((c) => c.id === value || c.slug === value);
  return cat?.name || value;
}

function buildCanonicalUrl({ pageType, post, slug, settings }) {
  const base = siteBaseUrl(settings);
  if (!base) return null;
  switch (pageType) {
    case 'home':
      return `${base}/`;
    case 'post-list':
      return `${base}/posts/`;
    case 'post-detail': {
      const raw = post?.canonical;
      if (raw && raw !== 'auto' && /^https?:\/\//.test(raw)) return raw;
      // Phase 9-i-f-b：cross-source mirror page → Blogger publishedUrl as canonical
      //   per docs/phase-9h-known-blockers.md §3（Blocker #1）
      //   對稱於 build-blogger.js 之 Phase 9-i-b2 修正
      if (post?.primaryPlatform === 'blogger') {
        const bloggerPublishedUrl = post.publish?.blogger?.publishedUrl;
        if (typeof bloggerPublishedUrl === 'string' && bloggerPublishedUrl !== '') {
          return bloggerPublishedUrl;
        }
      }
      return `${base}/posts/${post.slug}/`;
    }
    case 'category-list':
      return `${base}/categories/`;
    case 'category':
      return `${base}/categories/${slug}/`;
    case 'tag-list':
      return `${base}/tags/`;
    case 'tag':
      return `${base}/tags/${slug}/`;
    case '404':
    case 'design-system':
      return null;
    default:
      return null;
  }
}

function commonSeo({ pageType, settings, canonicalUrl }) {
  return {
    pageType,
    canonicalUrl,
    robots: 'index, follow',
    ogType: 'website',
    ogLocale: ogLocaleFromLanguage(settings.site?.language),
    ogImage: null,
    ogImageAlt: null,
    jsonLd: null,
    publishedTime: null,
    modifiedTime: null,
    keywords: null,
  };
}

function buildSeoForHome({ settings }) {
  const canonicalUrl = buildCanonicalUrl({ pageType: 'home', settings });
  const seo = commonSeo({ pageType: 'home', settings, canonicalUrl });
  if (canonicalUrl) {
    seo.jsonLd = {
      '@context': 'https://schema.org',
      '@type': 'WebSite',
      '@id': canonicalUrl,
      name: settings.site.siteName,
      url: canonicalUrl,
      description: settings.site.description,
      inLanguage: settings.site.language,
    };
  }
  return seo;
}

function buildSeoForPostList({ settings }) {
  const canonicalUrl = buildCanonicalUrl({ pageType: 'post-list', settings });
  return commonSeo({ pageType: 'post-list', settings, canonicalUrl });
}

// Phase 9-f-g-c: BlogPosting.mainEntity = Book（per docs/phase-9f-g-pre-plan.md §4-§5）
//   - 條件式：post.book object + (mediaType 缺省 OR === "book") + book.title non-empty
//   - 採 author fallback chain：authors[].displayName → localName → originalName → legacy book.author
//   - 不接 Periodical / DVD / YouTube / Netflix specific @type / library sameAs / @graph / Person.sameAs
//   - 與 build-blogger.js buildBloggerJsonLd() 兩端 mirror
function buildBookMainEntity(post) {
  const book = post.book;
  if (!book || typeof book !== 'object' || Array.isArray(book)) return null;
  if (book.mediaType && book.mediaType !== 'book') return null;
  if (typeof book.title !== 'string' || book.title.trim() === '') return null;
  const entity = { '@type': 'Book', name: book.title };
  // alternateName: non-empty titleEn / originalTitle; 1 → string；2 → array
  const altNames = [book.titleEn, book.originalTitle].filter(
    (v) => typeof v === 'string' && v.trim() !== '',
  );
  if (altNames.length === 1) entity.alternateName = altNames[0];
  else if (altNames.length > 1) entity.alternateName = altNames;
  // author: 優先 authors[] role 缺省或 === "author"；fallback chain name
  const authorPersons = (Array.isArray(book.authors) ? book.authors : [])
    .filter((a) => a && typeof a === 'object' && !Array.isArray(a))
    .filter((a) => !a.role || a.role === 'author')
    .map((a) => {
      const name = [a.displayName, a.localName, a.originalName].find(
        (v) => typeof v === 'string' && v.trim() !== '',
      );
      return name ? { '@type': 'Person', name } : null;
    })
    .filter(Boolean);
  if (authorPersons.length > 0) {
    entity.author = authorPersons;
  } else if (typeof book.author === 'string' && book.author.trim() !== '') {
    entity.author = [{ '@type': 'Person', name: book.author }];
  }
  // publisher: Organization
  if (typeof book.publisher === 'string' && book.publisher.trim() !== '') {
    entity.publisher = { '@type': 'Organization', name: book.publisher };
  }
  // datePublished: book.publishedYear → "YYYY"
  if (typeof book.publishedYear === 'number' && Number.isFinite(book.publishedYear)) {
    const year = Math.trunc(book.publishedYear);
    if (year >= 1000 && year <= 9999) entity.datePublished = String(year);
  }
  // isbn / image / bookEdition：non-empty 才出
  if (typeof book.isbn === 'string' && book.isbn.trim() !== '') entity.isbn = book.isbn;
  if (typeof book.coverImage === 'string' && book.coverImage.trim() !== '')
    entity.image = book.coverImage;
  if (typeof book.volumeLabel === 'string' && book.volumeLabel.trim() !== '')
    entity.bookEdition = book.volumeLabel;
  return entity;
}

function buildSeoForPostDetail({ settings, post }) {
  const canonicalUrl = buildCanonicalUrl({ pageType: 'post-detail', settings, post });
  const base = siteBaseUrl(settings);
  const ogImage = absolutizeUrl(post.cover, base);
  const seo = {
    ...commonSeo({ pageType: 'post-detail', settings, canonicalUrl }),
    ogType: 'article',
    ogImage,
    ogImageAlt: post.coverAlt || null,
    publishedTime: post.date || null,
    modifiedTime: post.updated || post.date || null,
    keywords: Array.isArray(post.tags) && post.tags.length > 0 ? post.tags : null,
  };
  // Phase 20260520-seo-2：robots meta precedence（per docs/seo-indexing-rules.md §3 / §6 SEO-2）
  //   優先序：
  //     1. post.seo.indexing (explicit；本批新增)
  //     2. contentKind === 'download' fallback (SEO-1；既有)
  //     3. default 'index, follow'（由 commonSeo spread 提供）
  //   非法 seo.indexing 值由 validate-content.js 之 invalid-seo-indexing 規則 (warning) 偵測；
  //   build 端對未匹配之值不主動 fallback，沿用 SEO-1 / default 行為
  const seoIndexing = post.seo && typeof post.seo.indexing === 'string' ? post.seo.indexing : null;
  if (seoIndexing === 'index') {
    seo.robots = 'index, follow';
  } else if (seoIndexing === 'noindex-follow') {
    seo.robots = 'noindex, follow';
  } else if (seoIndexing === 'noindex-nofollow') {
    seo.robots = 'noindex, nofollow';
  } else if (post.contentKind === 'download') {
    // Phase 20260520-seo-1 fallback：contentKind=download 視為導流漏斗後段頁
    //   - 保留 follow 以維持外向連結 PageRank flow（per docs/seo-indexing-rules.md §3 / §4）
    seo.robots = 'noindex, follow';
  }
  if (canonicalUrl) {
    // Phase 9-g-g-c: isPartOf 採最保守 site/blog 層級（per docs/phase-9g-g-pre-plan.md §5）
    //   - @type=Blog（schema.org 標準）；@id / url 依 post.primaryPlatform 指向對應平台 blog 首頁
    //   - 不使用 book / series / category 當 isPartOf（語義錯誤或當前無 ready post 觸發）
    //   - 與 build-blogger.js buildBloggerJsonLd() 兩端結構 mirror
    const blogSiteUrl =
      post.primaryPlatform === 'blogger'
        ? `${settings.site.bloggerSiteUrl}/`
        : `${settings.site.githubSiteUrl}/`;
    // Phase 9-g-g-d: mentions 接 relatedLinks + otherLinks（per docs/phase-9g-g-pre-plan.md §6）
    //   - 嚴格 pre-filter：array only + title non-empty + url non-empty
    //   - item @type 固定 WebPage；只輸出 @type / name / url 三欄位
    //   - 不映射 platform / kind / description / order / target / rel
    //   - 不臆造 specific subType（VideoObject / Book / Periodical 等）
    //   - 與 build-blogger.js buildBloggerJsonLd() 兩端 mirror
    const mentionsItems = [
      ...(Array.isArray(post.relatedLinks) ? post.relatedLinks : []),
      ...(Array.isArray(post.otherLinks) ? post.otherLinks : []),
    ]
      .filter(
        (entry) =>
          entry &&
          typeof entry === 'object' &&
          !Array.isArray(entry) &&
          typeof entry.title === 'string' &&
          entry.title.trim() !== '' &&
          typeof entry.url === 'string' &&
          entry.url.trim() !== '',
      )
      .map((entry) => ({ '@type': 'WebPage', name: entry.title, url: entry.url }));
    const bookMainEntity = buildBookMainEntity(post);
    seo.jsonLd = {
      '@context': 'https://schema.org',
      '@type': 'BlogPosting',
      '@id': canonicalUrl,
      headline: post.title,
      description: post.description || settings.site.description,
      datePublished: post.date,
      dateModified: post.updated || post.date,
      author: { '@type': 'Person', name: post.author || settings.site.author },
      mainEntityOfPage: canonicalUrl,
      inLanguage: settings.site.language,
      articleSection: findCategoryName(settings, post.category),
      isPartOf: {
        '@type': 'Blog',
        '@id': blogSiteUrl,
        name: settings.site.siteName,
        url: blogSiteUrl,
        inLanguage: settings.site.language,
      },
      ...(mentionsItems.length > 0 ? { mentions: mentionsItems } : {}),
      ...(bookMainEntity ? { mainEntity: bookMainEntity } : {}),
    };
    if (ogImage) seo.jsonLd.image = ogImage;
  }
  return seo;
}

